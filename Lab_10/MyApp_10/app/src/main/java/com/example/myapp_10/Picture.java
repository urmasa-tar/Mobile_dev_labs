package com.example.myapp_10;

public class Picture {
    int img;
    String str;

    public Picture(int img, String str) {
        this.img = img;
        this.str = str;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

}
